    private void calculateResBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_calculateResBtnMouseClicked
        if(validateData()){
            AeroCalculator ac=new AeroCalculator(Double.parseDouble(mainHeightInput.getText()), Double.parseDouble(mainWidthInput.getText()), Double.parseDouble(badInput.getText()), Double.parseDouble(jibHeightInput.getText()), Double.parseDouble(jibWidthInput.getText()), Double.parseDouble(lppInput.getText()), Double.parseDouble(speedInput.getText()), Double.parseDouble(angleInput.getText()), Double.parseDouble(densityInput.getText()), Double.parseDouble(freeboardInput.getText()), Double.parseDouble(hullBeamInput.getText()), Double.parseDouble(mastDiameterInput.getText()), Double.parseDouble(flattenerInput.getText()), Double.parseDouble(reefFunInput.getText()));
            EquilibriumCalculator ec=new EquilibriumCalculator(rc, ac);
            if(rf==null){
                rf=new ResultForm(this, ec);
                this.setVisible(false);
                rf.setVisible(true);
            }else{
                rf.setEc(ec);
                this.setVisible(false);
                rf.setVisible(true);
            }
                    
        }else{
            System.out.println("11");
        }
    }//GEN-LAST:event_calculateResBtnMouseClicked

