    private boolean validateData(){
        if(mainHeightInput.getText().isEmpty()|| mainWidthInput.getText().isEmpty()|| badInput.getText().isEmpty()){
            return false;
        }
        if(jibHeightInput.getText().isEmpty()|| jibWidthInput.getText().isEmpty()|| lppInput.getText().isEmpty()){
            return false;
        }
        if(speedInput.getText().isEmpty()|| angleInput.getText().isEmpty()|| densityInput.getText().isEmpty()){
            return false;
        }
        if(freeboardInput.getText().isEmpty()|| hullBeamInput.getText().isEmpty()|| mastDiameterInput.getText().isEmpty()){
            return false;
        }
        if(flattenerInput.getText().isEmpty()|| reefFunInput.getText().isEmpty()){
            return false;
        }
        if((Double.parseDouble(flattenerInput.getText())<0)||(Double.parseDouble(flattenerInput.getText())>1)){
            return false;
        }
        if((Double.parseDouble(reefFunInput.getText())<0)||(Double.parseDouble(reefFunInput.getText())>1)){
            return false;
        }
        return true;
    }
